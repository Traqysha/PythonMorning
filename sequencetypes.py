# List
list1 = ["Apple", "Mango", 5, 10.6]

print(list1)
print(type(list1))

print(list1[1])
print(list1[0:2])

print(list1*3)

# Tuples
mytuple = ("Mary", 15, "John")

print(mytuple*2)
print(mytuple[2])
print(type(mytuple))

# Dictionaries
devices = {1: "laptop", 2: "phone"}
print(type(devices))
print(devices.keys())
print(devices.values())